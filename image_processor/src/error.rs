//! # Модуль ошибок
//!
//! Этот модуль определяет перечисление `PluginError`, которое используется для централизованной обработки
//! всех возможных ошибок в процессе обработки изображений и загрузки плагинов.
//!
//! ## Обзор
//!
//! `PluginError` объединяет ошибки из различных источников:
//!
//! - Работа с изображениями (`image::ImageError`).
//! - Загрузка динамических библиотек (`libloading::Error`).
//! - Ошибки ввода-вывода (`std::io::Error`).
//! - Проблемы с C-строками (`NulError`).
//! - Специфичные ошибки домена (например, `OutOfRange`).
//!
//! Это позволяет API библиотеки возвращать единый тип ошибки, упрощая её использование.

use thiserror::Error;
use image::ImageError;
use std::ffi::NulError;
use std::io;

/// Перечисление всех возможных ошибок, возникающих в процессе обработки изображений.
///
/// Реализует `std::error::Error` через макрос `#[derive(Error)]` из `thiserror`,
/// что автоматически генерирует удобные сообщения об ошибках.
///
/// # Пример
///
/// ```
/// use std::path::Path;
/// match image_processor::process_image(
///     Path::new("input.png"),
///     Path::new("output.png"),
///     Path::new("libblur_plugin.so"),
///     Path::new("params.json"),
/// ) {
///     Ok(()) => println!("Успех!"),
///     Err(e) => eprintln!("Ошибка: {}", e),
/// }
/// ```
#[derive(Error, Debug)]
pub enum PluginError {
    /// Ошибка преобразования или обработки изображения.
    ///
    /// Возникает при повреждённом файле, поддерживаемом формате или других проблемах с библиотекой `image`.
    /// Автоматически преобразуется из `image::ImageError`.
    #[error("File not found")]
    ConversionError(#[from] ImageError),
    /// Ошибка загрузки динамической библиотеки (плагина).
    ///
    /// Возникает, когда файл плагина не найден, имеет неверную архитектуру,
    /// или не содержит необходимой функции `process_image`.
    /// Автоматически преобразуется из `libloading::Error`.
    #[error("Load library error")]
    LoadLibrary(#[from] libloading::Error),
    /// Индекс или значение вышло за допустимые пределы.
    ///
    /// Например, попытка доступа к пикселю с координатами вне границ изображения.
    #[error("Out of range")]
    OutOfRange,
    /// Строка содержит нулевой байт, что недопустимо для C-строк.
    ///
    /// Возникает, когда параметры JSON или путь к файлу содержат символ `\0`.
    /// Автоматически преобразуется из `std::ffi::NulError`.
    #[error("Null error")]
    Null(#[from] NulError),
    /// Ошибка ввода-вывода.
    ///
    /// Возникает при чтении/записи файлов (например, файл не найден, нет прав доступа).
    /// Автоматически преобразуется из `std::io::Error`.
    #[error("IO error")]
    IO(#[from] io::Error),
    /// Неизвестная ошибка, для которой не найдено более точного описания.
    ///
    /// Используется как запасной вариант, когда причина ошибки не может быть определена.
    #[error("Unknown error")]
    Unknown,
}
