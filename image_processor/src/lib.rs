//! # Image Processor
//!
//! Это библиотека для обработки изображений с поддержкой плагинов. Она загружает изображение,
//! применяет к нему внешний плагин (например, размытие или зеркальное отражение) и сохраняет результат.
//!
//! Основная функция — [`process_image`] — координирует чтение изображения, загрузку плагина,
//! передачу параметров и сохранение результата.
//!
//! ## Архитектура
//!
//! - Использует динамическую загрузку плагинов через модуль [`plugin_loader`].
//! - Поддерживает изображения в формате RGBA.
//! - Принимает параметры обработки в формате JSON.
//! - Сохраняет результат в формате PNG.
//!
//! ## Поддерживаемые плагины
//!
//! Плагины должны быть скомпилированы как динамические библиотеки (`.dll`, `.so`, `.dylib`)
//! и экспортировать функцию `process_image` с C-совместимой сигнатурой:
//!
//! ```c
//! void process_image(
//!     unsigned long width,
//!     unsigned long height,
//!     unsigned char* rgba_data,
//!     const char* params_json
//! );
//! ```
//!
//! Примеры таких плагинов: `blur_plugin`, `mirror_plugin`.
//!
//! ## Параметры обработки
//!
//! Параметры передаются в виде JSON-файла. Формат зависит от плагина:
//!
//! - **Blur Plugin**: `{"radius": 2, "iterations": 3}`
//! - **Mirror Plugin**: `{"horizontal": true, "vertical": false}`
//!
//! Библиотека просто читает файл и передаёт его содержимое плагину.
//!
//! ## Обработка ошибок
//!
//! Все ошибки обрабатываются с помощью пользовательского типа [`error::PluginError`],
//! который покрывает:
//!
//! - Ошибки ввода-вывода (чтение/запись файлов).
//! - Ошибки парсинга строк (например, недопустимые символы в JSON).
//! - Ошибки загрузки плагинов (неверный путь, отсутствие функции).
//!
//! ## Пример использования
//!
//! ```no_run
//! use std::path::Path;
//! image_processor::process_image(
//!     Path::new("input.png"),
//!     Path::new("output.png"),
//!     Path::new("plugins/libblur_plugin.so"),
//!     Path::new("params.json"),
//! ).expect("Failed to process image");
//! ```
//!
//! ## Внутренние модули
//!
//! - [`plugin_loader`]: Загрузка и вызов внешних плагинов.
//! - [`error`]: Определение типов ошибок.
//!
//! Эта библиотека предназначена для использования как зависимость в других проектах
//! или как часть CLI-утилиты.
//!

#![warn(missing_docs)]

/// Обработчик ошибок
pub mod error;
/// Загрузчик плагинов
pub mod plugin_loader;

use image::ImageFormat;
use plugin_loader::Plugin;
use std::ffi::CString;
use std::path::Path;

/// Обрабатывает изображение с использованием внешнего плагина.
///
/// Эта функция является основной точкой входа в библиотеку.
///
/// # Параметры
///
/// - `input_path`: путь к входному изображению (поддерживаемые форматы: PNG, JPEG, BMP и др.).
/// - `output_path`: путь, по которому будет сохранён результат (всегда в формате PNG).
/// - `plugin_path`: путь к динамической библиотеке плагина (`.so`, `.dll`, `.dylib`).
/// - `params_path`: путь к JSON-файлу с параметрами для плагина.
///
/// # Процесс обработки
///
/// 1. Открывает изображение и преобразует его в формат RGBA8.
/// 2. Читает параметры из JSON-файла и оборачивает их в `CString` для передачи в C.
/// 3. Загружает плагин по указанному пути.
/// 4. Получает интерфейс плагина (включая функцию `process_image`).
/// 5. Вызывает функцию обработки плагина, передавая ей размеры, пиксели и параметры.
/// 6. Сохраняет изменённое изображение в формате PNG.
///
/// # Ошибки
///
/// Возвращает [`error::PluginError`] в следующих случаях:
///
/// - Изображение не может быть открыто или сохранено.
/// - JSON-файл содержит недопустимые данные (например, нулевой байт).
/// - Плагин не может быть загружен (неверный путь, отсутствует функция `process_image`).
///
/// # Безопасность
///
/// - Вызов плагина происходит через `unsafe` (внутри `plugin_loader`), так как он взаимодействует
///   с сырой памятью и C-интерфейсами.
/// - Предполагается, что плагин корректно обрабатывает указатели и не вызывает UB.
///
/// # Пример
///
/// ```no_run
/// use std::path::Path;
/// match image_processor::process_image(
///     Path::new("in.jpg"),
///     Path::new("out.png"),
///     Path::new("libblur_plugin.so"),
///     Path::new("blur_params.json"),
/// ) {
///     Ok(()) => println!("Обработка успешна!"),
///     Err(e) => eprintln!("Ошибка: {}", e),
/// }
/// ```
///
/// # Примечания
///
/// - Изображение всегда сохраняется как PNG, независимо от входного формата.
/// - Альфа-канал (прозрачность) полностью поддерживается.
/// - Для работы требуется, чтобы плагин был совместим с текущей архитектурой и ABI.

pub fn process_image(
    input_path: &Path,
    output_path: &Path,
    plugin_path: &Path,
    params_path: &Path,
) -> Result<(), error::PluginError> {
    let mut rgba8 = image::open(input_path)?.into_rgba8();

    let params_str = std::fs::read_to_string(params_path)?;
    let params_str = CString::new(params_str)?;
    let plugin = Plugin::new(plugin_path)?;
    let plugin_interfase = plugin.interface()?;

    unsafe {
        (plugin_interfase.process_image)(
            rgba8.width(),
            rgba8.height(),
            rgba8.as_mut_ptr(),
            params_str.as_ptr(),
        );
    }

    rgba8.save_with_format(output_path, ImageFormat::Png)?;
    Ok(())
}
